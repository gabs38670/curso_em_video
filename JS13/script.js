var c = 1;
//Estrutura de repetição simple com While que significa enquanto
//No while primeiro faz o teste e depois executa o bloco
while(c <= 6){
    console.log(`Passo ${c}`)
    c++
}

//DO Wilhe primeiro executa o bloco e depois faz o teste
do {
    console.log(`Passo ${c}`)
    c++
} while (c <= 6)